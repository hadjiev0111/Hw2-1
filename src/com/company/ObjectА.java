package com.company;

public class ObjectА {
    private int year;
    private String name;
    private Amir Amir;
    private Colors colors;

    public ObjectА(int year, String name, Amir amir, Colors objectE) {
        this.year = year;
        this.name = name;
        this.Amir = amir;
        this.colors = objectE;
    }

    public int getYear() {
        return year;
    }

    public String getName() {
        return name;
    }

    public Amir getAmir() {
        return Amir;
    }

    public Colors getColors() {
        return colors;
    }
}
